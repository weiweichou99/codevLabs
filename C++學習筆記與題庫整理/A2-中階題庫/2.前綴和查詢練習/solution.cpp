#include <iostream>
#include <vector>
using namespace std;

int main() {
    // ========== 第一步：讀取輸入 ==========
    int n, q;
    cin >> n >> q;

    // ========== 第二步：讀取陣列（1-based） ==========
    // 使用 1-based 索引，a[1] ~ a[n]
    vector<long long> a(n + 1);  // 多開一格，a[0] 不使用
    for (int i = 1; i <= n; i++) {
        cin >> a[i];
    }
    // 範例：a[1..5] = [1, 3, -2, 5, 4]

    // ========== 第三步：建立前綴和陣列（1-based） ==========
    // S[i] = a[1] + a[2] + ... + a[i]
    // S[0] = 0 作為邊界，這樣查詢 [1, r] 時可以用 S[r] - S[0]
    
    vector<long long> S(n + 1);
    S[0] = 0;  // 邊界：前 0 個數的總和是 0
    
    for (int i = 1; i <= n; i++) {
        S[i] = S[i-1] + a[i];
    }
    // 範例：
    // a = [_, 1,  3, -2,  5,  4]  (索引 0 不用)
    // S = [0, 1,  4,  2,  7, 11]
    //      ↑  ↑   ↑   ↑   ↑   ↑
    //      0  1  1+3 4-2 2+5 7+4

    // ========== 第四步：處理每個查詢 ==========
    for (int i = 0; i < q; i++) {
        int L, R;
        cin >> L >> R;
        // L, R 是查詢的區間（從 1 開始計算）

        // 區間 [L, R] 的總和 = S[R] - S[L-1]
        long long sum = S[R] - S[L-1];
        
        cout << sum << endl;
    }
    // 範例查詢：
    // 查詢 [1, 3]：S[3] - S[0] = 2 - 0 = 2 ✓
    // 查詢 [2, 5]：S[5] - S[1] = 11 - 1 = 10 ✓

    return 0;
}

/*
 * ========== 前綴和的好處 ==========
 * 
 * 如果沒有前綴和，每次查詢都要用迴圈加總：
 *   for (int i = l; i <= r; i++) sum += arr[i];
 *   → 每次查詢需要 O(r-l) 時間
 *   → q 次查詢總共需要 O(q * n) 時間，太慢！
 * 
 * 有了前綴和：
 *   sum = prefix[r] - prefix[l-1];
 *   → 每次查詢只需要 O(1) 時間
 *   → q 次查詢總共只需要 O(q) 時間，超快！
 * 
 * ========== 記憶口訣 ==========
 * 
 * 「區間總和 = 右邊前綴和 - 左邊前一個的前綴和」
 * 
 */
