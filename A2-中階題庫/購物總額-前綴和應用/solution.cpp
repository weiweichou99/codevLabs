#include <bits/stdc++.h>
using namespace std;

int main() {
    int N, Q;
    cin >> N >> Q;

    // ========== 1. 讀取每天的花費 A[i]，並同時構建前綴和陣列 S ==========
    // 前綴和陣列 S：S[i] 儲存 A[1] + ... + A[i] 的和
    // 陣列大小為 N+1，S[0] = 0
    vector<long long> S(N + 1, 0);
    
    for (int i = 1; i <= N; ++i) {
        int a;
        cin >> a;
        S[i] = S[i - 1] + a;  // 構建前綴和：S[i] = S[i-1] + A[i]
    }
    // 範例：A = [10, 20, 30, 40, 50]
    //       S = [0, 10, 30, 60, 100, 150]
    //            ↑   ↑   ↑   ↑    ↑    ↑
    //           S[0] S[1] S[2] S[3] S[4] S[5]
    //            0   10  10+20 30+30 60+40 100+50

    // ========== 2. 查詢 Q 次 ==========
    for (int k = 0; k < Q; ++k) {
        int L, R;  // 查詢區間的起始 L 和結束 R
        cin >> L >> R;

        // ========== 3. 計算區間 [L, R] 的總和 (Sum) ==========
        // Sum(L, R) = S[R] - S[L-1]
        // L 和 R 都是 1-based 索引，正好對應 S 陣列的索引
        long long total_expense = S[R] - S[L - 1];
        
        cout << total_expense << endl;
    }
    // 範例查詢：
    // [1, 3]：S[3] - S[0] = 60 - 0 = 60 ✓
    // [2, 5]：S[5] - S[1] = 150 - 10 = 140 ✓
    // [3, 3]：S[3] - S[2] = 60 - 30 = 30 ✓

    return 0;
}

